CREATE TABLE IF NOT EXISTS stock_movements (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    product_id      INT NOT NULL,
    movement_type   ENUM('entrada_inicial','entrada_manual','salida_venta','ajuste_manual','anulacion_venta','devolucion') NOT NULL,
    quantity        INT NOT NULL,
    stock_before    INT NULL,
    stock_after     INT NULL,
    reference_type  ENUM('order','sale','manual') NOT NULL DEFAULT 'manual',
    reference_id    INT NULL,
    notes           TEXT NULL,
    created_by      INT NULL,
    created_at      TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_stock_movements_product FOREIGN KEY (product_id) REFERENCES products(id),
    CONSTRAINT fk_stock_movements_user FOREIGN KEY (created_by) REFERENCES users(id) ON DELETE SET NULL,
    INDEX idx_stock_movements_product_created (product_id, created_at),
    INDEX idx_stock_movements_reference (reference_type, reference_id),
    INDEX idx_stock_movements_type (movement_type)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS ubigeo (
    id              INT AUTO_INCREMENT PRIMARY KEY,
    department_code VARCHAR(2) NOT NULL,
    department_name VARCHAR(120) NOT NULL,
    province_code   VARCHAR(4) NOT NULL,
    province_name   VARCHAR(120) NOT NULL,
    district_code   VARCHAR(6) NOT NULL,
    district_name   VARCHAR(120) NOT NULL,
    UNIQUE KEY uq_ubigeo_district (district_code),
    INDEX idx_ubigeo_department (department_code, department_name),
    INDEX idx_ubigeo_province (province_code, province_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

